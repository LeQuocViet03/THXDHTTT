<h1>QUY ĐỊNH SỬ DỤNG PHÒNG THỰC HÀNH TIN HỌC</h1>
<p>1. Sinh viên phải tuân thủ các quy định về an toàn.</p>
<p>2. Không được sử dụng phần mềm trái phép.</p>
<p>3. Không được ăn uống trong phòng thực hành.</p>
<p>4. Không được đập phá thiết bị trong phòng.</p>
<p>5. Không được đem thiết bị đi.</p>
<p>6. Khi rút dây dẫn để sử dụng thì cuối giờ nhớ trả về ban đầu.</p>
<p>7. Khi có sự cố bất kì thì phải lập tức báo cáo cho giảng viên gần đó hoặc báo báo trên hệ thống này.</p>
<p>8. Không tự ý sữa chữa khi thiết bị đang hư hỏng.</p>
<br>
<p>Rất mong quý giảng viên và sinh viên thực hiện nghiêm túc các quy định sử dụng trên. Nếu không nghiêm túc thực hiện mà gây ra thiệt hại sẽ phải chịu kỷ luật theo quy định của ban quản lý phòng và nhà trường.</p>
<p>Kính chúc quý giảng viên và sinh viên có 1 ngày làm việc và học tập có hiệu quả tại phòng học của chúng tôi!</p>